package ex_01_Java_Based;

public class Lab013_Println_Vs_Print {
    public static void main(String[] args) {

        System.out.println("Println mean go to next line");
        System.out.print("Use Print only that means it will not go next line ");
        System.out.println();

        System.out.println("Hi");
        System.out.println("Bye");

        System.out.print("Hi ");
        System.out.print("Bye");
    }
}
